#include "libft.h"

char	*ft_strcat(char *dest, const char *src)
{
	size_t	i;
	size_t	len;

	if (!dest && !src)
		return (NULL);
	if (!dest)
		return ((char *)src);
	if (!src)
		return (dest);
	i = 0;
	len = ft_strlen(dest);
	while (src[i])
	{
		dest[len + i] = src[i];
		i++;
	}
	dest[len + i] = '\0';
	return (dest);
}
