#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	i;
	int	last;

	if (!s)
		return (NULL);
	i = 0;
	last = -1;
	while (s[i])
	{
		if (s[i] == (char)c)
			last = i;
		i++;
	}
	if (last != -1)
		return ((char *)s + last);
	if (!c)
		return ((char *)s + i);
	return (NULL);
}
